package area.guias.pfc;

public class CommentDate {
	String year;
	String month; 
	String day;
	String hoursMinSec;
	
	public CommentDate(){
		year = null;
		month = null;
		day = null;
		hoursMinSec = null;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public String getDay() {
		return day;
	}

	public void setDay(String day) {
		this.day = day;
	}

	public String getHoursMinSec() {
		return hoursMinSec;
	}

	public void setHoursMinSec(String hoursMinSec) {
		this.hoursMinSec = hoursMinSec;
	}

	
	
	
}
