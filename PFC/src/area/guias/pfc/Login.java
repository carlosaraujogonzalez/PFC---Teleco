package area.guias.pfc;

public class Login {

	String email, password;
		
	public Login(){
		email = null;
		password = null;
	}
		
	public String getEmail() {
		return email;
	}

	public void setEmail(String str) {
		email = str;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String str) {
		password = str;
	}     
		
}
